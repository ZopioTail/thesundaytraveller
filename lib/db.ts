export async function searchContent(query: string, type?: 'posts' | 'news' | 'destinations') {
  const results: any = {}

  if (!type || type === 'posts') {
    results.posts = mockPosts.filter(post =>
      post.title.toLowerCase().includes(query.toLowerCase()) ||
      post.excerpt.toLowerCase().includes(query.toLowerCase()) ||
      post.tags.some(tag => tag.toLowerCase().includes(query.toLowerCase()))
    )
  }

  if (!type || type === 'news') {
    results.news = mockNews.filter(news =>
      news.title.toLowerCase().includes(query.toLowerCase()) ||
      news.excerpt.toLowerCase().includes(query.toLowerCase())
    )
  }

  if (!type || type === 'destinations') {
    results.destinations = mockDestinations.filter(dest =>
      dest.name.toLowerCase().includes(query.toLowerCase()) ||
      dest.description.toLowerCase().includes(query.toLowerCase())
    )
  }

  return results
}




